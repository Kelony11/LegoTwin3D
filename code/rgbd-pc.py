def point_cloud(depth, fov_x):
    # adapted from: https://blender.stackexchange.com/a/211600
    # Don't use any of the functions provided by Open3D
    # Don't use create_point_cloud_from_rgbd_image
    # Don't use the pinhole instrinsic parameters
    # I don't know why they are wrong, but they misalign

    # Distance factor from the camera focal angle
    factor = 2.0 * np.tan(fov_x / 2.0)

    rows, cols = depth.shape
    c, r = np.meshgrid(np.arange(cols), np.arange(rows), sparse=True)
    valid = (depth > 0)

    # Negate Z (the camera Z is at the opposite)
    z = -np.where(valid, depth, np.nan)
    ratio = max(rows, cols)
    x = -np.where(valid, factor * z * (c - (cols / 2)) / ratio, 0)
    y = np.where(valid, factor * z * (r - (rows / 2)) / ratio, 0)
    ones = np.ones_like(z)
    return np.dstack((x, y, z, ones))


def read_transparent_png_depth(filename):
    # for depth png only, very hacky
    # Code from https://stackoverflow.com/a/41896175

    image_4channel = cv2.imread(filename, cv2.IMREAD_UNCHANGED)

    # in the .png depth image generated by Blender, all 4 channels are the same
    depth_channel = image_4channel[:, :, 0]
    # normalize 16-bit depth
    depth_channel = depth_channel.astype(np.float32) / 65535.
    # print(np.max(depth_channel), np.min(depth_channel))

    # need to inverse and * 8, 8 is the max depth in generate_rgbd.py
    depth_channel_inv = (1. - depth_channel) * 8.
    # print(np.max(depth_channel_inv), np.min(depth_channel_inv))

    depth_channel_final = (depth_channel > 0) * depth_channel_inv
    return depth_channel_final.astype(np.float32)


def read_transparent_png(filename):
    # for color png only, not for depth
    # Code from https://stackoverflow.com/a/41896175

    image_4channel = cv2.imread(filename, cv2.IMREAD_UNCHANGED)

    alpha_channel = image_4channel[:, :, 3]
    bgr_channels = image_4channel[:, :, :3]

    rgb_channels = cv2.cvtColor(bgr_channels, cv2.COLOR_BGR2RGB)
    # print("rgb_channels:" , np.max(rgb_channels), np.min(rgb_channels))

    # White Background Image
    white_background_image = np.ones_like(rgb_channels, dtype=np.uint8) * 255

    # Alpha factor
    alpha_factor = alpha_channel[:, :, np.newaxis].astype(np.float32) / 255.0
    # print("alpha_factor: ", np.max(alpha_factor), np.min(alpha_factor))
    alpha_factor = np.concatenate((alpha_factor, alpha_factor, alpha_factor), axis=2)

    # Transparent Image Rendered on White Background
    base = rgb_channels.astype(np.float32) * alpha_factor
    white = white_background_image.astype(np.float32) * (1 - alpha_factor)
    final_image = base + white
    return final_image.astype(np.uint8)


def normalize(vector):
    return vector / np.linalg.norm(vector)


