def create_pcd_perspective(rgb_image_path, depth_image_path, camera_path, idx, noise=0):
    color = read_transparent_png(rgb_image_path)
    depth = read_transparent_png_depth(depth_image_path)

    height, width = color.shape[:2]

    with open(camera_path) as f:
        camera_info = json.load(f)

    rot, trans, fov_x = parse_json_camera_info(camera_info, height, width, idx)
    
    if noise > 0:
        # Add random noise to translation
        noise_vec = np.random.uniform(-noise, noise, 3)
        trans += noise_vec

    points = point_cloud(depth, fov_x)

    points = np.reshape(points, (points.shape[0] * points.shape[1], 4))

    # valid only when the z coordinate is not np.nan
    valid = ~np.isnan(points[:, 2])
    points = points[valid]

    extrinsic = np.eye(4)

    extrinsic[:3, :3] = rot
    extrinsic[:3, -1] = trans

    verts = np.dot(points, extrinsic[:3].T)

    color = np.divide(color.astype(np.float64), 255)
    color = np.reshape(color, (color.shape[0] * color.shape[1], 3))
    color = color[valid]

    pcd = o3d.geometry.PointCloud()
    pcd.points = o3d.utility.Vector3dVector(verts)
    pcd.colors = o3d.utility.Vector3dVector(color)

    return extrinsic, pcd


